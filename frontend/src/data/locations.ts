// Default districts for provinces without detailed data
const DEFAULT_DISTRICTS: District[] = [
  { value: 'trung-tam', label: 'Trung tâm' },
  { value: 'khac', label: 'Khác' },
];

export function getDistrictsByProvince(provinceValue: string): District[] {
  return DISTRICTS[provinceValue] || DEFAULT_DISTRICTS;
}

export function getProvinceLabel(value: string): string {
  return PROVINCES.find(p => p.value === value)?.label || value;
}

export function getDistrictLabel(provinceValue: string, districtValue: string): string {
  const districts = getDistrictsByProvince(provinceValue);
  return districts.find(d => d.value === districtValue)?.label || districtValue;
}
